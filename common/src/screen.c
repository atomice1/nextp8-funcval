/* screen_clear - Clear off-screen buffer to black */
void screen_clear(void)
{
    memset(screen_offscreen_buffer, 0, sizeof(screen_offscreen_buffer));
    screen_cursor_x = 0;
    screen_cursor_y = 0;
}

/* screen_set_pixel - Set a pixel in off-screen buffer */
void screen_set_pixel(uint16_t x, uint16_t y, uint8_t color)
{
    if (x >= 128 || y >= 128)
        return;

    uint16_t offset = y * 64 + x / 2;
    uint8_t *ptr = &screen_offscreen_buffer[offset];

    if (x & 1) {
        /* Right pixel: high nibble */
        *ptr = (*ptr & 0x0F) | ((color & 0x0F) << 4);
    } else {
        /* Left pixel: low nibble */
        *ptr = (*ptr & 0xF0) | (color & 0x0F);
    }
}

/* screen_fill_rect - Fill rectangle with color */
void screen_fill_rect(uint16_t x, uint16_t y, uint16_t width, uint16_t height, uint8_t color)
{
    for (uint16_t row = 0; row < height; row++) {
        for (uint16_t col = 0; col < width; col++) {
            screen_set_pixel(x + col, y + row, color);
        }
    }
}

/* screen_scroll - Scroll off-screen buffer up by given number of pixels */
void screen_scroll(uint16_t pixels)
{
    if (pixels == 0 || pixels >= 128)
        return;

    /* Move rows up */
    uint16_t src_offset = pixels * 64;
    uint16_t copy_bytes = (128 - pixels) * 64;
    memmove(screen_offscreen_buffer, screen_offscreen_buffer + src_offset, copy_bytes);

    /* Clear bottom rows */
    memset(screen_offscreen_buffer + copy_bytes, 0, src_offset);
}

/* screen_flip - Copy off-screen buffer to back buffer, then flip */
void screen_flip(void)
{
    uint8_t *back_buffer = (uint8_t *)_BACK_BUFFER_BASE;
    volatile uint8_t *vfrontreq = (volatile uint8_t *)_VFRONTREQ;

    /* Copy off-screen buffer to back buffer */
    memcpy(back_buffer, screen_offscreen_buffer, sizeof(screen_offscreen_buffer));

    /* Read current vfront bit */
    uint8_t target = *vfrontreq;

    /* Toggle vfrontreq bit (bit 0) to request flip */
    target ^= 0x01;
    *vfrontreq = target;

    /* Wait for flip to complete */
    target &= 0x01;
    while ((*vfrontreq & 0x01) != target)
        ;
}

/* screen_putchar - Draw 3x5 character at current cursor position */
void screen_putchar(char c)
{
    /* Handle special characters */
    if (c == '\n') {
        /* Sleep 100ms */
        usleep(100000);

        screen_cursor_x = 0;
        screen_cursor_y += 6;  /* 5 pixel char + 1 pixel space */

        if (screen_cursor_y > 122) {
            /* Scroll screen up */
            uint16_t scroll_amount = screen_cursor_y - 122;
            screen_scroll(scroll_amount);
            screen_cursor_y = 122;
        }
        return;
    }

    if (c == '\r') {
        screen_cursor_x = 0;
        return;
    }

    if (c < ' ' || c > '~')
        return;  /* Ignore control characters */

    /* Get character index (offset from space) */
    uint8_t char_idx = c - ' ';
    const uint8_t *glyph = &font_3x5[char_idx * 10];

    /* Draw 3x5 character (5 rows, each row has 2 bytes encoding 3 pixels) */
    for (int row = 0; row < 5; row++) {
        uint8_t byte0 = glyph[row * 2];
        uint8_t byte1 = glyph[row * 2 + 1];

        /* Draw left pixel (byte 0 low nibble) */
        uint8_t pixel = byte0 & 0x0F;
        if (pixel)
            screen_set_pixel(screen_cursor_x, screen_cursor_y + row, pixel);

        /* Draw middle pixel (byte 0 high nibble) */
        pixel = byte0 >> 4;
        if (pixel)
            screen_set_pixel(screen_cursor_x + 1, screen_cursor_y + row, pixel);

        /* Draw right pixel (byte 1 low nibble) */
        pixel = byte1 & 0x0F;
        if (pixel)
            screen_set_pixel(screen_cursor_x + 2, screen_cursor_y + row, pixel);
    }

    /* Advance cursor by 4 pixels (3 pixel char + 1 pixel space) */
    screen_cursor_x += 4;
    if (screen_cursor_x >= 128) {
        screen_cursor_x = 0;
        screen_cursor_y += 6;

        if (screen_cursor_y > 122) {
            uint16_t scroll_amount = screen_cursor_y - 122;
            screen_scroll(scroll_amount);
            screen_cursor_y = 122;
        }
    }
}

/* screen_puts - Draw null-terminated string at cursor */
void screen_puts(const char *str)
{
    while (*str) {
        screen_putchar(*str++);
    }
}

/* screen_print_dec - Print 32-bit value as decimal at cursor */
void screen_print_dec(uint32_t value)
{
    char buffer[12];
    int pos = 0;

    if (value == 0) {
        screen_putchar('0');
        return;
    }

    /* Convert to decimal (reverse order) */
    while (value > 0) {
        buffer[pos++] = '0' + (value % 10);
        value /= 10;
    }

    /* Print in correct order */
    for (int i = pos - 1; i >= 0; i--) {
        screen_putchar(buffer[i]);
    }
}

/* screen_print_hex_nibble - Print single hex nibble */
static void screen_print_hex_nibble(uint8_t nibble)
{
    nibble &= 0x0F;
    if (nibble < 10)
        screen_putchar('0' + nibble);
    else
        screen_putchar('A' + (nibble - 10));
}

/* screen_print_hex_byte - Print 8-bit value as hex (2 digits) */
void screen_print_hex_byte(uint8_t value)
{
    screen_print_hex_nibble(value >> 4);
    screen_print_hex_nibble(value);
}

/* screen_print_hex_word - Print 16-bit value as hex (4 digits) */
void screen_print_hex_word(uint16_t value)
{
    for (int i = 3; i >= 0; i--) {
        screen_print_hex_nibble(value >> (i * 4));
    }
}

/* screen_print_hex_long - Print 32-bit value as hex (8 digits) */
void screen_print_hex_long(uint32_t value)
{
    for (int i = 7; i >= 0; i--) {
        screen_print_hex_nibble(value >> (i * 4));
    }
}
